public class PrimitiveVariables {
    public static void main(String[] args) {
        // This is a comment
        //System.out.println("Hello, world!");
        //System.out.print("Hello ");
        //System.out.print("again.");
        // The following are declared variables
        int age;
        double gpa;
        // The following are declared and initialized variables
        char letterGrade = 'A';
        boolean csMajor = true;

        age = 28;
        gpa = 3.8;

        System.out.println("His gpa is " + gpa);
        System.out.print("His age is " + age);
        System.out.print("\nHis grade is " + letterGrade);
        System.out.print("\nHe is a CS Major (True or False): " + csMajor);
    }
}
