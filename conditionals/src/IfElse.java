public class IfElse {
    public static void main(String[] args) {
        boolean answer1 = true;
        boolean answer2 = false;

        System.out.println(answer1);
        System.out.println(answer2);

        System.out.println(!answer1);
        System.out.println(!answer2);
        System.out.println(answer1 != answer2);
    }
}