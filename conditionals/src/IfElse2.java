public class IfElse2 {
    public static void main(String[] args) {
        int age = 20;

        if (age >= 21) {
            System.out.println("Age is greater than 21.");
        }

        if (age % 2 == 0) {
            System.out.println("Age is an even number.");
        }

        if (age != 21) {
            System.out.println("Age is not equal to 21.");
        }

        if (age == 20) {
            System.out.println("Age is 20.");
        }
    }
}